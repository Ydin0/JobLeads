// Get job board option by ID
export function getJobBoardById(id: string) {
    return jobBoardOptions.find(board => board.id === id)
}

// Get multiple job board options by IDs
export function getJobBoardsByIds(ids: string[]) {
    return ids.map(id => getJobBoardById(id)).filter(Boolean)
}
